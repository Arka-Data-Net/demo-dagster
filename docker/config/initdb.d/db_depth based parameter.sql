CREATE TABLE drillingparameter (
  Well varchar(100) NOT NULL,
  Depth float(10) DEFAULT NULL,
  TVD float(10) DEFAULT NULL,
  Inclination float(10) DEFAULT NULL,
  Azimuth float(10) DEFAULT NULL,
  Elevation float(10) DEFAULT NULL,
  Vertical_Section float(10) DEFAULT NULL,
  Northing float(10) DEFAULT NULL,
  Easting float(10) DEFAULT NULL
)